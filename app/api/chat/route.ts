function sseEncode(event: string, data: unknown): Uint8Array {
  return new TextEncoder().encode(`event: ${event}\ndata: ${JSON.stringify(data)}\n\n`)
}

export async function POST(request: Request) {
  const apiKey = getApiKey()
  if (!apiKey) {
    return new Response(JSON.stringify({ error: 'Chưa cấu hình GOOGLE_API_KEY.' }), {
      status: 500,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  let body: { message?: string; history?: HistoryItem[] }
  try {
    body = await request.json()
  } catch {
    return new Response(JSON.stringify({ error: 'Body không hợp lệ.' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  const userMessage = (body.message ?? '').trim()
  if (!userMessage) {
    return new Response(JSON.stringify({ error: 'Thiếu nội dung câu hỏi.' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  const history = Array.isArray(body.history) ? body.history : []
  const relevantContext = await buildRelevantContext(userMessage)

  const contents = [
    { role: 'user', parts: [{ text: buildSystemPrompt(relevantContext) }] },
    ...history.map((msg) => ({
      role: msg.role === 'assistant' ? 'model' : 'user',
      parts: [{ text: msg.content ?? msg.text ?? '' }],
    })),
  ]

  const upstream = await fetch(`${CHAT_API_BASE}:streamGenerateContent?alt=sse&key=${apiKey}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ contents, generationConfig: GENERATION_CONFIG, safetySettings: SAFETY_SETTINGS }),
  })

  if (!upstream.ok || !upstream.body) {
    const detail = await upstream.text().catch(() => '')
    return new Response(JSON.stringify({ error: `Lỗi từ AI (${upstream.status}). ${detail.slice(0, 200)}` }), {
      status: 502,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  // Re-stream the upstream Gemini SSE as our own chunk/done/error events.
  const stream = new ReadableStream<Uint8Array>({
    async start(controller) {
      const reader = upstream.body!.getReader()
      const decoder = new TextDecoder()
      let buffer = ''
      let emitted = false

      try {
        while (true) {
          const { done, value } = await reader.read()
          if (done) break

          buffer += decoder.decode(value, { stream: true })
          const lines = buffer.split('\n')
          buffer = lines.pop() ?? ''

          for (const line of lines) {
            const trimmed = line.trim()
            if (!trimmed.startsWith('data:')) continue
            const payload = trimmed.slice(5).trim()
            if (!payload || payload === '[DONE]') continue
            try {
              const data = JSON.parse(payload)
              const chunk = data?.candidates?.[0]?.content?.parts?.[0]?.text ?? ''
              if (chunk) {
                emitted = true
                controller.enqueue(sseEncode('chunk', { text: chunk }))
              }
            } catch {
              // ignore partial / keep-alive lines
            }
          }
        }

        if (!emitted) {
          controller.enqueue(sseEncode('error', { message: 'AI không trả về nội dung.' }))
        } else {
          controller.enqueue(sseEncode('done', {}))
        }
      } catch (error) {
        const message = error instanceof Error ? error.message : 'Streaming bị gián đoạn.'
        controller.enqueue(sseEncode('error', { message }))
      } finally {
        controller.close()
      }
    },
  })

  return new Response(stream, {
    headers: {
      'Content-Type': 'text/event-stream; charset=utf-8',
      'Cache-Control': 'no-cache, no-transform',
      Connection: 'keep-alive',
    },
  })
}
