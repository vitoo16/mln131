import { promises as fs } from 'node:fs'
import path from 'node:path'

// Server-side chat endpoint: runs RAG over the course embeddings and streams
// Gemini's answer back as Server-Sent Events. The API key stays on the server.

export const runtime = 'nodejs'
export const maxDuration = 60

const EMBEDDING_MODEL = 'gemini-embedding-2-preview'
const CHAT_API_BASE = 'https://api.shopaikey.com/v1/models/gemini-3.5-flash'
const EMBED_API_BASE = `https://api.shopaikey.com/v1beta/models/${EMBEDDING_MODEL}`

const GENERATION_CONFIG = {
  temperature: 0.45,
  topK: 40,
  topP: 0.9,
  maxOutputTokens: 4096,
}
const SAFETY_SETTINGS = [{ category: 'HARM_CATEGORY_HARASSMENT', threshold: 'BLOCK_NONE' }]

type EmbeddingItem = { source: string; text: string; embedding: number[] }
type HistoryItem = { role: string; text?: string; content?: string }

function getApiKey() {
  return process.env.GOOGLE_API_KEY || process.env.NEXT_PUBLIC_GOOGLE_API_KEY || ''
}

// Load + cache embeddings from data/embeddings.json (generated by `npm run ingest`).
let embeddingsCache: EmbeddingItem[] | null = null
async function loadEmbeddings(): Promise<EmbeddingItem[]> {
  if (embeddingsCache) return embeddingsCache
  let loaded: EmbeddingItem[] = []
  try {
    const file = path.join(process.cwd(), 'data', 'embeddings.json')
    const raw = await fs.readFile(file, 'utf8')
    const data = JSON.parse(raw)
    loaded = Array.isArray(data) ? data : data.embeddings ?? []
  } catch {
    loaded = []
  }
  embeddingsCache = loaded
  return loaded
}

function cosineSimilarity(a: number[], b: number[]): number {
  if (a.length !== b.length) return 0
  let dot = 0
  let magA = 0
  let magB = 0
  for (let i = 0; i < a.length; i++) {
    dot += a[i] * b[i]
    magA += a[i] * a[i]
    magB += b[i] * b[i]
  }
  magA = Math.sqrt(magA)
  magB = Math.sqrt(magB)
  return magA && magB ? dot / (magA * magB) : 0
}

const EMBED_TIMEOUT_MS = 6000

async function embedQuery(text: string): Promise<number[]> {
  const apiKey = getApiKey()
  if (!apiKey) return []

  // Cap the RAG embedding round-trip: if the upstream is slow/overloaded, skip
  // context retrieval rather than making the whole answer wait on it.
  const controller = new AbortController()
  const timeout = setTimeout(() => controller.abort(), EMBED_TIMEOUT_MS)
  try {
    const response = await fetch(`${EMBED_API_BASE}:embedContent?key=${apiKey}`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ content: { parts: [{ text }] } }),
      signal: controller.signal,
    })
    if (!response.ok) return []
    const data = await response.json()
    return data?.embedding?.values ?? []
  } catch {
    return []
  } finally {
    clearTimeout(timeout)
  }
}

const RAG_TOP_K = 6
const RAG_MAX_CHARS_PER_CHUNK = 1200

async function buildRelevantContext(userMessage: string): Promise<string> {
  try {
    const embeddings = await loadEmbeddings()
    if (embeddings.length === 0) return ''

    const userEmbedding = await embedQuery(userMessage)
    if (userEmbedding.length === 0) return ''

    const top = embeddings
      .map((item) => ({ text: item.text, score: cosineSimilarity(userEmbedding, item.embedding) }))
      .sort((a, b) => b.score - a.score)
      .slice(0, RAG_TOP_K)

    if (top.length === 0) return ''

    const blocks = top
      .map((ctx, i) => `[Trích đoạn ${i + 1}]\n${ctx.text.slice(0, RAG_MAX_CHARS_PER_CHUNK).trim()}`)
      .join('\n\n')

    return `\n\n===== TRÍCH DẪN TỪ GIÁO TRÌNH (căn cứ ưu tiên để trả lời) =====\n${blocks}\n===== HẾT TRÍCH DẪN =====`
  } catch {
    return ''
  }
}

